function apagar_ext($local){	
	foreach(glob($local) as $listar){
		unlink($listar);		
	}
	if(file_exists(strtr($local, array('*'=>'')))){
		unlink(strtr($local, array('*'=>'')));
	}
	
}
function buscar_dados($pasta){
	
	$xml = simplexml_load_file("root/".$pasta."/cadastro.xml");
	
	$dados['cod'] = $xml->cod;
	$dados['nome'] = $xml->responsavel;
	$dados['cpf'] = $xml->cpf;
	$dados['nasc'] = $xml->nascimento;
	return $dados;
}